#include <iostream>
using namespace std;

int main()
{
    long long x, y;
    cin >> x >> y;
    cout << x << " + " << y << " = " << (x + y) << "\n";
    cout << x << " * " << y << " = " << (x * y) << "\n";
    cout << x << " - " << y << " = " << (x - y) << "\n";
    return 0;
}